package pkg;

public class Alphabet {

	public static void main(String[] args) {
// find  a character is alphabet or not
		
	
		char c='P';
		if((c>='a' && c<='z') || (c>='A' && c<='Z'))
		{
			System.out.println("c is alphabet");
		}
		else
		{
			System.out.println("c is not alphabet");
		}

	}

}
