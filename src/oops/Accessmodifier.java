public class Accessmodifier {
	private int a=8;
	int b=9;
	protected int p=7;
	public int k=4;
	

	public static void main(String[] args) {
	
		Accessmodifier ac=new Accessmodifier();
		System.out.println(ac.a);
		System.out.println(ac.b);
		System.out.println(ac.k);
		

	}

}
